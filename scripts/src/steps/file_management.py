import os
import subprocess
import tempfile
from pathlib import Path
from typing import Dict, Set, Tuple

from scripts.src.steps.step import EnvConfig
from scripts.src.utils.logger import get_logger

CONCURRENT_FETCHES: int = 10


logger = get_logger(__name__)


def fetch_files(
    env: EnvConfig,
    dataset_file_map: Dict[str, Set[Tuple[Path, Path]]],
    datasets_folder: Path,
) -> None:
    for dataset, files in dataset_file_map.items():
        if len(files) == 0:
            continue

        commands = [
            env.conda_activation_str,
            f"datalad get {' '.join(str(src) for (src, _) in files)} \
-J {CONCURRENT_FETCHES}",
        ]
        shell_command = " && ".join(commands)
        try:
            logger.info(f"Fetching {len(files)} files in {dataset}...")
            subprocess.run(
                shell_command, shell=True, check=True, cwd=datasets_folder / dataset
            )
            logger.info(f"Fetched {len(files)} files in {dataset}")
        except subprocess.CalledProcessError as e:
            logger.error(f"Failed to fetch file with datalad: {e}")
            logger.error(f"Subprocess stdout: {e.stdout}")
            logger.error(f"Subprocess stderr: {e.stderr}")
            raise


def copy_files(
    env: EnvConfig, file_pairs: Set[Tuple[Path, Path]], dataset: Path
) -> None:
    for (src, dst) in file_pairs:
        logger.info(f"Copying file pair {src!s}: {dst!s}")
    logger.info(
        f"Copying {len(file_pairs)} files with datalad specfile in dataset {dataset}"
    )
    # We use a specfile because
    # (1) copying files one by one creates numerous commits (generated by DataLad)
    # (2) copying recursively isn't the best strategy, as you can't filter
    spec_lines = [
        f"{os.path.relpath(src, start=dataset)}\0{os.path.relpath(dst, start=dataset)}"
        for (src, dst) in file_pairs
    ]

    if len(spec_lines) == 0:
        return

    specfile_path: Path

    with tempfile.NamedTemporaryFile("w", delete=False) as specfile:
        specfile.write("\n".join(spec_lines))
        specfile_path = Path(specfile.name)

    commands = [
        env.conda_activation_str,
        f"datalad copy-file --specs-from {specfile_path} -d . \
-m 'Copied {len(file_pairs)} files'",
    ]
    shell_command = " && ".join(commands)
    try:
        subprocess.run(shell_command, shell=True, check=True, cwd=dataset)
        logger.info(f"Copied files using specfile in dataset {dataset}")
    except subprocess.CalledProcessError as e:
        logger.error(f"Failed to copy file with datalad: {e}")
        logger.error(f"Subprocess stdout: {e.stdout}")
        logger.error(f"Subprocess stderr: {e.stderr}")
        raise
    finally:
        os.remove(specfile_path)


def datalad_save(env: EnvConfig, dest_dataset: Path, message: str) -> None:
    commands = [
        env.conda_activation_str,
        f"datalad save -m '{message}'",
    ]
    shell_command = " && ".join(commands)

    logger.info(f"Running shell command: {shell_command}")
    try:
        subprocess.run(shell_command, shell=True, check=True, cwd=dest_dataset)
    except subprocess.CalledProcessError as e:
        logger.error(f"Subprocess failed: {e}")
        logger.error(f"Subprocess stdout: {e.stdout}")
        logger.error(f"Subprocess stderr: {e.stderr}")
        raise e

    return


def git_unannex_and_save(
    env: EnvConfig, dest_dataset: Path, unannex_match: str, message: str
) -> None:
    commands = [
        env.conda_activation_str,
        f"git annex unannex {unannex_match}",
        f"datalad save -m '{message}'",
    ]
    shell_command = " && ".join(commands)

    logger.info(f"Running shell command: {shell_command}")
    try:
        subprocess.run(shell_command, shell=True, check=True, cwd=dest_dataset)
    except subprocess.CalledProcessError as e:
        logger.error(f"Subprocess failed: {e}")
        logger.error(f"Subprocess stdout: {e.stdout}")
        logger.error(f"Subprocess stderr: {e.stderr}")
        raise e

    return

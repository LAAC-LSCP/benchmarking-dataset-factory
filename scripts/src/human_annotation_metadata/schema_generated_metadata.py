"""
Schema for metadata generated by `get_human_annotation_metadata.py`
"""

from typing import List, Optional

from pydantic import BaseModel


class GeneratedColumn(BaseModel):
    column: str
    values: List[str] | None
    annotated_duration_ms: int
    duration_from_samples_ms: int
    number_of_samples: Optional[int] = None
    num_of_non_empty_segments: int


class GeneratedSet(BaseModel):
    name: str
    columns: List[GeneratedColumn]


class GeneratedDataset(BaseModel):
    name: str
    sets: List[GeneratedSet]


class GeneratedDatasets(BaseModel):
    datasets: List[GeneratedDataset]

"""
Small script for getting datasets metadata for the interspeech submission
Uses the `manually_annotated_metadata.json`, which is made from the outputs in
the human_annotation_metadata folder, generated by `get_human_annotation_metadata`
and also validated both manually and using the `validate_manual_metadata` script

Gets the dataset, set, annotated_total_duration, sampled_total_duration

Use NOT the metannots as a source of truth BUT the manually annotated data
"""

from enum import StrEnum
from typing import Dict, List, TypedDict

import click
import pandas as pd

from scripts.src.custom_types import DatasetType
from scripts.src.human_annotation_metadata.schema_manual_metadata import (
    dataset_model_factory,
)
from scripts.src.metadata import get_generated_metadata, get_manual_metadata
from scripts.src.utils.constants import DATASETS_FOLDER
from scripts.src.utils.logger import get_logger


class Row(TypedDict):
    dataset: str
    set: str
    annotated_total_duration_min: float
    sampled_total_duration_min: float
    num_of_segments_non_empty: int
    num_of_samples: int | None


class ManualCol(StrEnum):
    ADDRESSEE = "addressee_cols"
    VCM = "vcm_cols"
    VTC = "vtc_cols"
    TRANSCRIPTION = "transcription_cols"


COL_TYPE_MAPPING: Dict[DatasetType, ManualCol] = {
    DatasetType.ADDRESSEE: ManualCol.ADDRESSEE,
    DatasetType.VOCAL_MATURITY: ManualCol.VCM,
    DatasetType.VTC: ManualCol.VTC,
    DatasetType.TRANSCRIPTION: ManualCol.TRANSCRIPTION,
}


logger = get_logger(__name__)


@click.command()
@click.option(
    "--output-path",
    type=click.Path(),
    help="Output path of dataframe",
)
@click.option(
    "--dataset-type",
    type=click.Choice([t.value for t in DatasetType]),
    help="Type of dataset",
)
def get_datasets_metadata_table(output_path: str, dataset_type: str):
    generated_data = get_generated_metadata()
    manual_data = dataset_model_factory(generated_data)(**get_manual_metadata())

    rows: List[Row] = []

    for dataset in DATASETS_FOLDER.iterdir():
        dataset_name = dataset.name
        if not dataset.is_dir():
            continue

        generated_dataset = next(
            (d for d in generated_data.datasets if d.name == dataset_name), None
        )
        assert generated_dataset is not None
        manual_dataset = next(
            (d for d in manual_data.datasets if d.name == dataset_name), None
        )
        assert manual_dataset is not None

        for manual_set in manual_dataset.sets:
            annotated_duration_ms = 0
            duration_from_samples_ms = 0
            num_non_empty_segments = 0

            generated_set = next(
                (s for s in generated_dataset.sets if s.name == manual_set.name), None
            )
            assert generated_set is not None

            relevant_cols: List[str] = manual_set.__getattribute__(
                COL_TYPE_MAPPING[DatasetType(dataset_type)]
            )

            generated_cols = [
                col for col in generated_set.columns if col.column in relevant_cols
            ]

            annotated_duration_ms += sum(
                [c.annotated_duration_ms for c in generated_cols]
            )
            duration_from_samples_ms += sum(
                [c.duration_from_samples_ms for c in generated_cols]
            )
            num_non_empty_segments += sum(
                [c.num_of_non_empty_segments for c in generated_cols]
            )

            if annotated_duration_ms == 0:
                continue

            rows.append(
                {
                    "dataset": dataset.name,
                    "set": manual_set.name,
                    "annotated_total_duration_min": ms_to_m(annotated_duration_ms),
                    "sampled_total_duration_min": ms_to_m(duration_from_samples_ms),
                    "num_of_segments_non_empty": num_non_empty_segments,
                    "num_of_samples": next(
                        (c.number_of_samples for c in generated_cols), None
                    ),  # constant
                }
            )

    datasets = pd.DataFrame(rows)
    datasets = datasets.sort_values(by=["dataset", "set"])

    logger.info("Saving...")
    datasets.to_csv(output_path, index=True)
    logger.info("Done!")

    return


def ms_to_m(ms: int) -> float:
    return round(ms / 1000 / 60, 3)


if __name__ == "__main__":
    get_datasets_metadata_table()
